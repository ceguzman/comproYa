    @Test
    @Transactional
    void createProduct() throws Exception {
        int databaseSizeBeforeCreate = productRepository.findAll().size();
        // Create the Product
        restProductMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(product)))
            .andExpect(status().isCreated());

        // Validate the Product in the database
        List<Product> productList = productRepository.findAll();
        assertThat(productList).hasSize(databaseSizeBeforeCreate + 1);
        Product testProduct = productList.get(productList.size() - 1);
        assertThat(testProduct.getNameProduct()).isEqualTo(DEFAULT_NAME_PRODUCT);
        assertThat(testProduct.getDescriptionProduct()).isEqualTo(DEFAULT_DESCRIPTION_PRODUCT);
        assertThat(testProduct.getProductPrice()).isEqualTo(DEFAULT_PRODUCT_PRICE);
        assertThat(testProduct.getProductAumount()).isEqualTo(DEFAULT_PRODUCT_AUMOUNT);
    }

